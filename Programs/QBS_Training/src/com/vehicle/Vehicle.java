package com.vehicle;

public class Vehicle 
{
	 	private String manufacturer;
	    protected int year;
	    
	    public void setManufacturer(String manufacturer) {
	        this.manufacturer = manufacturer;
	    }
	    
	    public String getManufacturer() {
	        return manufacturer;
	    }
	    
	    public void setYear(int year) {
	        this.year = year;
	    }
	    
	    public int getYear() {
	        return year;
	    }

}
